    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:String jenisku = jenis.getText();
        int umurku = Integer.parseInt(textField3.getText());
        String namaku = textField2.getText();
        String alamatku = textField1.getText();
        
        int selectedRow = jTable2.getSelectedRow();
        
        if (selectedRow >= 0) {
            // Panggil fungsi updateData dari ControllerMotor
            boolean updated = pgw.updateData(selectedRow, namaku, alamatku, umurku);

            if (updated) {
                // Data berhasil diperbarui, perbarui tampilan tabel
                jTable2.setModel(pgw.showData());
            } else {
                // Data tidak dapat diperbarui (indeks salah), tampilkan pesan kesalahan jika diperlukan
                JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diperbarui.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Tidak ada baris yang dipilih, tampilkan pesan kesalahan jika diperlukan
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diperbarui.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = jTable2.getSelectedRow();
    
        if (selectedRow >= 0) {
            // Get the index of the selected row in the JTable
            int dataIndex = jTable2.convertRowIndexToModel(selectedRow);
        
            // Call the deleteData method to delete the data
            boolean deleted = pgw.deleteData(dataIndex);
        
            if (deleted) {
                // Refresh the JTable to reflect the updated data
                jTable2.setModel(pgw.showData());
            } else {
                // Handle the case where data deletion failed
                JOptionPane.showMessageDialog(this, "Data deletion failed.");
            }
        } else {
            // Handle the case where no row is selected
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
        }
        
        textField1.setText("");
        textField2.setText("");
        textField3.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

